    public function play(int $n): void
    {
        if (!isset(self::$categories[$n])) {
            http_response_code(404);
            echo 'Категория не найдена';
            return;
        }
        $categoryName = self::$categories[$n];

        // Уже решённые задачи id для этой категории
        $solvedIds = [];
        if (!empty($_SESSION['progress'])) {
            foreach ($_SESSION['progress'] as $taskId => $status) {
                $task = Task::find($taskId);
                if ($task && $task['category'] === $categoryName) {
                    $solvedIds[] = $taskId;
                }
            }
        }

        // Если была отправка ответа POST, обрабатываем текущую
        if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['task_id'])) {
            $taskId     = (int)$_POST['task_id'];
            $userAnswer = trim($_POST['answer']);
            $task       = Task::find($taskId);
            $isCorrect  = strcasecmp($userAnswer, $task['answer']) === 0;
            $_SESSION['progress'][$taskId] = $isCorrect ? 'correct' : 'wrong';
            $solvedIds[] = $taskId;
            $feedback   = $isCorrect ? 'Верно!' : 'Неверно. Правильный ответ: ' . htmlspecialchars($task['answer']);
        }

        // Получаем следующую задачу
        $nextTask = Task::nextUnsolvedByCategory($categoryName, $solvedIds);

        include __DIR__ . '/../Views/category_task.php';
    }
